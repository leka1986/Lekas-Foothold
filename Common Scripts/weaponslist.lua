function checkWeaponsList(airbase)
    if Era == 'Coldwar' then
        env.info('Cold war is on')

        if airbase then
            local storage = STORAGE:FindByName(airbase)
            if storage then
                env.info('CW-STORAGE: resetting '..airbase)
                local aircraft = storage:GetInventory()
                for name,_ in pairs(aircraft) do storage:SetItem(name,0) end
                for _,plane in ipairs(allowedPlanes) do storage:SetItem(plane,1073741823) end
                for _,weapon in ipairs(restrictedWeapons) do
                    local amt = storage:GetItemAmount(weapon)
                    if amt > 0 then
                        storage:RemoveItem(weapon,amt)
                    end
                end
            else
                env.info('CW-STORAGE: no storage for '..airbase)
            end
            return
        end

        local airbaseList, seen = {}, {}

        local zones = bc:getZones()
        for _, zone in ipairs(zones) do
            local z = bc:getZoneByName(zone.zone)
            local abName = z and z.airbaseName
            if abName and not seen[abName] then
                seen[abName] = true
                airbaseList[#airbaseList + 1] = abName
            end
        end

        for _, n in ipairs({"CVN-72","CVN-73","Tarawa","CVN-59","CVN-74","HMS Invincible","FOB ALPHA"}) do
            if not seen[n] and IsGroupActive(n) then
                seen[n] = true
                airbaseList[#airbaseList + 1] = n
            end
        end

        for _, airbaseName in ipairs(airbaseList) do
            local storage = STORAGE:FindByName(airbaseName)
            if storage then
                env.info('CW-STORAGE: resetting '..airbaseName)
                local aircraft = storage:GetInventory()
                for name,_ in pairs(aircraft) do storage:SetItem(name,0) end
                for _,plane in ipairs(allowedPlanes) do storage:SetItem(plane,1073741823) end
                for _,weapon in ipairs(restrictedWeapons) do
                    local amount = storage:GetItemAmount(weapon)
                    if amount > 0 then
                        storage:RemoveItem(weapon,amount)
                    end
                end
            else
                env.info('CW-STORAGE: no storage for '..airbaseName)
            end
        end
    else
        local airbaseList, seen = {}, {}
        for _, z in ipairs(bc:getZones()) do
            local ab = bc:getZoneByName(z.zone) and bc:getZoneByName(z.zone).airbaseName
            if ab and not seen[ab] then
                seen[ab] = true
                airbaseList[#airbaseList + 1] = ab
            end
        end
        for _, n in ipairs({"CVN-72","CVN-73","Tarawa","CVN-59","CVN-74","HMS Invincible"}) do
            if not seen[n] and IsGroupActive(n) then
                seen[n] = true
                airbaseList[#airbaseList + 1] = n
            end
        end
        for _, airbaseName in ipairs(airbaseList) do
            local storage = STORAGE:FindByName(airbaseName)
            if storage then
                for _, wp in ipairs(restockWeapons) do
                    local okAmt, amt = pcall(function() return storage:GetItemAmount(wp) end)
                    if (not okAmt) or (amt == nil) or (amt == 0) or (amt >= 0 and amt < 100000) then
                        local ok = pcall(function() storage:SetItem(wp, 1073741823) end)
                        if ok then
                            --env.info('ADDED '..airbaseName..' '..(type(wp) == 'table' and '{'..table.concat(wp, ',')..'}' or tostring(wp))..' = 1073741823')
                        else
                            env.info('MISSING '..(type(wp) == 'table' and '{'..table.concat(wp, ',')..'}' or tostring(wp)))
                        end
                    end
                end

                for _, ac in ipairs(restockAircraft) do
                    local okAmt, amt = pcall(function() return storage:GetItemAmount(ac) end)
                    if (not okAmt) or (amt == nil) or (amt >= 0 and amt < 100000) then
                        local ok = pcall(function() storage:SetItem(ac, 1073741823) end)
                        if ok then
                            --env.info('ADDED '..airbaseName..' '..(type(ac) == 'table' and '{'..table.concat(ac, ',')..'}' or tostring(ac))..' = 1073741823')
                        else
                            env.info('MISSING '..(type(ac) == 'table' and '{'..table.concat(ac, ',')..'}' or tostring(ac)))
                        end
                    end
                end
            else
                env.info('CW-STORAGE: no storage for '..airbaseName)
            end
        end
    end
end
