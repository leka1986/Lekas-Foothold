local resMap = Warehouse.getResourceMap()
function DumpWeapons()
  local storage = STORAGE:FindByName("Bitburg")
  if not storage then return end
  local found = {}
  for itemName, ws in pairs(resMap) do
    if ws[1] == 4 then                       -- weapons only
      local amount = storage:GetItemAmount(itemName)  -- -1 if truly unlimited
      if amount ~= 0 and amount ~= nil then           -- skip zero / absent items
        env.info(("{%d,%d,%d,%d} %s = %d")
          :format(ws[1], ws[2], ws[3], ws[4], itemName, amount))
        found[ws[1]..','..ws[2]..','..ws[3]..','..ws[4]] = true
      end
    end
  end
  for _, v in ipairs(LookUpTable) do
    local key = v[1]..','..v[2]..','..v[3]..','..v[4]
    if not found[key] then
      env.info(("MISSING {%d,%d,%d,%d}")
        :format(v[1], v[2], v[3], v[4]))
    end
  end
end
